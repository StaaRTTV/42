void	ft_printnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		ft_printchar('-');
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_printnbr(nb / 10);
		nb = nb % 10;
	}
	if (nb < 10)
	{
		ft_printchar(nb + 48);
	}
}