int	ft_sqrt(int nb)
{
	int	div;

	div = 0;
	if (nb < 0)
		return (0);
	if (nb == 1)
		return (1);
	while (div < nb / 2)
	{
		if (div * div == nb)
			return (div);
		div++;
	}
	return (0);
}
